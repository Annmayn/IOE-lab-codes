TITLE PROG TO READ STRING
	.MODEL SMALL
	.STACK 32
	.DATA
MAX_CHAR DB 50
ACT_CHAR DB ?
STRING DB 51 DUP(?)
	.CODE 
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	MOV AH,0AH
	MOV DX,OFFSET MAX_CHAR
	INT 21H
	
	MOV DL,0DH
	MOV AH,02H
	INT 21H
	
	MOV DL,0AH
	MOV AH,02H
	INT 21H
	
	MOV BL,ACT_CHAR
	MOV STRING[BX],'$'
	MOV DX,OFFSET STRING
	MOV AH,09H
	INT 21H
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN