TITLE REVERSE
	.MODEL SMALL
	.STACK 128
	.DATA
MAX_CHAR DB 50
ACT_CHAR DB ?
STRING DB 50 DUP(0)
NEWLN DB 0DH,0AH,'$'
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,0AH
	MOV DX,OFFSET MAX_CHAR
	INT 21H
	
	MOV CH,0
	MOV CL,ACT_CHAR
	MOV BX,OFFSET STRING
	MOV DH,0
REVERSE:
	MOV DL,[BX]
	PUSH DX
	INC BX
LOOP REVERSE
	
	MOV AH,09
	MOV DX,OFFSET NEWLN
	INT 21H
	
	MOV CH,0
	MOV CL,ACT_CHAR
	MOV AH,02
DISPLAY:
	POP DX
	INT 21H
LOOP DISPLAY
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN