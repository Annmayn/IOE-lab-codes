TITLE READ STRING AND DISPLAY CHARACTER
	.MODEL SMALL
	.STACK 32
	.DATA
MAX_CHAR DB 24
ACT_CHAR DB ?
STRING DB 24 DUP(?)
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	;READ STRING
	MOV AH,0AH
	MOV DX,OFFSET MAX_CHAR
	INT 21H
	
	;SCROLL ENTIRE WINDOW UP
	MOV AX,0600H
	MOV CX,0
	MOV DX,184FH
	MOV BH,07H
	INT 10H
	
	;PREPARATION FOR OUTPUT
	MOV AH,02H
	MOV SI,OFFSET STRING
	MOV CH,0
	MOV CL,ACT_CHAR
	MOV DI,011AH		;CURSOR LOCATION

OUTPUT:
	;MOVE CURSOR TO THE CENTER
	MOV DX,DI
	MOV BX,DI
	INC BH
	MOV DI,BX
	MOV BH,0
	INT 10H
	
	;DISPLAY ONE CHARACTER
	MOV DL,[SI]
	INT 21H
	
	;POINT TO NEXT CHARACTER
	INC SI
LOOP OUTPUT

	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN
	