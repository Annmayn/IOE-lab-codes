TITLE COUNT VOWELS
	.MODEL SMALL
	.STACK 64
	.DATA
MAX_CHAR DB 255
ACT_CHAR DB ?
INP DB 256 DUP('$')
NEWLN DB 0DH,0AH,'$'
VCNT DW 0
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,0AH				;READ STRING
	MOV DX, OFFSET MAX_CHAR
	INT 21H
	
	MOV BX,OFFSET INP
VCOUNT:
	MOV DL,[BX]
	CMP DL,41H
	JE DOCOUNT
	CMP DL,45H
	JE DOCOUNT
	CMP DL,49H
	JE DOCOUNT
	CMP DL,4FH
	JE DOCOUNT
	CMP DL,55H
	JE DOCOUNT
	JMP SKIPCOUNT
DOCOUNT:
	INC VCNT
SKIPCOUNT:
	INC BX
	CMP DL,'$'
	JE POSTCOUNT
	JMP VCOUNT
	
POSTCOUNT:
	MOV AX,VCNT			;VCOUNT IS IN HEX
	MOV BX,10				;USED IN DIVIDING
	MOV CX,0				;COUNTER INITIALIZATION
CONVERT:
	MOV DX,0
	DIV BX
	ADD DX,30H
	PUSH DX
	INC CX
	CMP AX,0
	JE EXIT
	JMP CONVERT
EXIT:
	MOV DX,OFFSET NEWLN
	MOV AH,09
	INT 21H 
	
	MOV AH,02H
DISPLAY:
	POP DX
	INT 21H
	LOOP DISPLAY
	
	MOV AX,4C00H
	INT 21H
	
MAIN ENDP
	END MAIN
	
	
	
	
	