TITLE STRING DISPLAY WITH READING FUNCTION
	.MODEL SMALL
	.STACK 32
	.DATA
SENT DB "Programming is Fun"
LEN DW $-SENT
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	MOV CX,LEN
	MOV AH,02H
	MOV BX,OFFSET SENT
	NEXT:MOV DL,[BX]
	INC BX
	INT 21H
	LOOP NEXT
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN