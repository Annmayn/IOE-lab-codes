TITLE DISPLAY STRING IN CLEAR SCREEN
	.MODEL SMALL
	.STACK 32
	.DATA
MAX_CHAR DB 60
ACT_CHAR DB ?
STRING DB 60 DUP(?)
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	MOV AH,0AH
	MOV DX,OFFSET MAX_CHAR
	INT 21H
	
	MOV AX,0600H
	MOV CX,0
	MOV DX,184FH
	MOV BH,07H
	INT 10H
	
	MOV AH,02H
	MOV BH,0
	MOV DX,0C22H
	INT 10H
	
	MOV AH,09H
	MOV DX,OFFSET STRING
	MOV BL,ACT_CHAR
	MOV BH,0
	MOV STRING[BX],'$'
	INT 21H 
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN