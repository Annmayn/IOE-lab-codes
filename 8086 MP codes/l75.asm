TITLE CHANGE CASE
	.MODEL SMALL
	.STACK 32
	.DATA
MAX_CHAR DB 50
ACT_CHAR DB ?
STRING DB 50 DUP(?)
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	MOV DX,OFFSET MAX_CHAR
	MOV AH,0AH
	INT 21H
	MOV BL,ACT_CHAR
	MOV BH,0H
	MOV STRING[BX],'$'
	
	MOV DL,0DH
	MOV AH,02H
	INT 21H
	
	MOV DL,0AH
	MOV AH,02H
	INT 21H
	
	;PROCESSING THE STRING
	MOV CX,BX
	MOV BX,OFFSET STRING
NEXT: CMP BYTE PTR [BX],97
	JL SKIP
	CMP BYTE PTR[BX],122
	JG SKIP
	SUB BYTE PTR [BX],20H
SKIP: INC BX
	LOOP NEXT
	
	MOV DX,OFFSET STRING
	MOV AH,09H
	INT 21H 
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN