TITLE VOWEL DISPLAY
	.MODEL SMALL
	.STACK 32
	.DATA
EVENMSG DB "EVEN VOWELS$"
ODDMSG DB "ODD VOWELS$"
NEWLN DB 0DH,0AH,'$'
MAX_CHAR DB 50
ACT_CHAR DB ?
STRING DB 51 DUP('$')
	.CODE
MAIN PROC FAR
	MOV AX,@DATA
	MOV DS,AX
	
	MOV DX,OFFSET MAX_CHAR
	MOV AH,0AH
	INT 21H
	
	MOV BX,OFFSET STRING
	MOV CH,0
	MOV CL,ACT_CHAR
	MOV DX,0
L1:
	MOV AL,[BX]
	CMP AL,'A'
	JE INCREMENT
	CMP AL,'E'
	JE INCREMENT
	CMP AL,'I'
	JE INCREMENT
	CMP AL,'O'
	JE INCREMENT
	CMP AL,'U'
	JE INCREMENT
	JMP SKIP
INCREMENT:
	INC DX
SKIP:
	INC BX
	LOOP L1
	
	PUSH DX
	MOV DX,OFFSET NEWLN
	MOV AH,09
	INT 21H
	POP DX
	
	MOV AX,DX
	MOV DH,2
	DIV DH
	CMP AH,0
	JE EVENM
	MOV DX,OFFSET ODDMSG
	JMP DISPLAY
EVENM:
	MOV DX,OFFSET EVENMSG
DISPLAY:
	MOV AH,09
	INT 21H
	
	MOV AX,4C00H
	INT 21H
MAIN ENDP
	END MAIN